public class Lista2Prblema15 {
  public static void main(String[] args) {

      int[][] matriz = new int[10][10];

      int i;
      int j;

      for(i = 0; i < 10; i++) {
          for(j = 0; j < 10; j++) {
              matriz [i][j] = 0;
              System.out.println(matriz[i][j]+"");
          }
      }
  }
}